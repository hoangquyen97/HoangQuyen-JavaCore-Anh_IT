
public class Cau1 { // khai báo lớp chính
    public static void main(String[] args) { // phương thức main — điểm bắt đầu của chương trình
        //Dùng for để in ra màn hình các số từ 1 → 100
        System.out.println("In các số từ 1 đến 100");
        for (int i = 1; i <= 100; i++) { // khởi tạo i=1; điều kiện i<=100; sau mỗi vòng tăng i lên 1
            System.out.print(i + " "); // in giá trị i và thêm dấu cách
        }
    }
}